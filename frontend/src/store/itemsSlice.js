import { createSlice } from "@reduxjs/toolkit";

const itemsSlice = createSlice({
  name: "items",
  initialState: [],
  reducers: {
    addInitialItems: (state, action) => {
      return action.payload;
    },
  },
});

// Named Export
export const itemsAction = itemsSlice.actions;

// Default Export
export default itemsSlice;
